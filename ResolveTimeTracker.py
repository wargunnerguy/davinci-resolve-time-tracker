    def animate():
        # Breathing green dot while tracking; amber when paused; gray when off.
        try:
            if poller.connected and poller.running:
                t = (math.sin(phase["v"]) + 1) / 2.0
                g = int(80 + t * 160)
                col = "#%02x%02x%02x" % (8, g, 46)
                phase["v"] += 0.18
            elif poller.connected:
                col = "#e0a83a"
            else:
                col = "#777777"
            dot_canvas.itemconfig(dot, fill=col, outline=col)
            if mini["dot_canvas"] is not None and mini["dot"] is not None:
                mini["dot_canvas"].itemconfig(mini["dot"], fill=col, outline=col)
            # Pill border follows the same status color (amber when paused, etc.)
            if mini["frame"] is not None:
                mini["frame"].config(highlightbackground=col, highlightcolor=col)
        except Exception:
            pass
        root.after(60, animate)

    def bring_to_front():
        # Clicking the Resolve menu item while we're already running touches
        # RAISE_FILE; pull the existing window up and focus it (nice UX vs. a
        # silent no-op).
        try:
            if os.path.exists(RAISE_FILE):
                os.remove(RAISE_FILE)
                root.deiconify()
                root.lift()
                root.attributes("-topmost", poller.always_on_top())
                root.after(50, root.focus_force)
        except Exception:
            pass

    def loop():
        try:
            poller.tick()
            touch_lock()
            refresh()
            bring_to_front()
            # Follow the OS theme live when set to "System".
            if poller.theme() == "system" and system_is_dark() != built_system_dark:
                log("System theme changed; rebuilding UI")
                result["restart"] = True
                root.destroy()
                return
        except Exception:
            log_exception("ui loop")
        root.after(TICK_INTERVAL_MS, loop)

    def on_quit():
        try:
            if mini["active"]:
                poller.update_config(mini_pos=[root.winfo_x(), root.winfo_y()])
            poller.save(force=True)
            poller.stop_reader()
        except Exception:
            log_exception("on_quit")
        result["restart"] = False
        root.destroy()

    update_pause_label()
    root.protocol("WM_DELETE_WINDOW", on_quit)
    animate()
    loop()
    if poller.config.get("start_mini"):
        root.after(200, enter_mini)  # open straight into the pill if configured
    root.mainloop()
    return result["restart"]


