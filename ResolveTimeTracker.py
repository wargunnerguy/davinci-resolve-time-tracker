        # (the native Tk scrollbar can't be fully skinned on Windows).
        sb = tk.Canvas(panel, width=9, bg=PANEL_BG, highlightthickness=0, bd=0)
        sb.pack(side="right", fill="y")
        canvas.pack(side="left", fill="both", expand=True)
