    def draw_pages(proj):
        c = pages_canvas
        c.delete("all")
        w = c.winfo_width()
        if w <= 1:
            w = 360
        r = poller.rec(proj) if proj else None
        pages = r["pages"] if r else {}
        total = sum(pages.values()) if pages else 0
        cur = poller.currency()
        rows = [(p, pages.get(p, 0)) for p in PAGES if pages.get(p, 0) > 0]
        rowh = 28
        barh = 20
        iconx, namex, timex, barx0, barx1 = 9, 32, 96, 158, w - 10
        active = poller.current_page if (poller.connected and poller.running) else None
        for i, (p, secs) in enumerate(rows):
            y = i * rowh + 4
            midy = y + barh / 2
            if p == active:
                # subtle highlight behind the active page's row
                c.create_rectangle(2, y - 4, w - 2, y + barh + 4,
                                   fill=ROW_HL, outline="")
            # active page -> COLOR icon, inactive -> BW; fall back to the
            # generated glyph for pages without custom art.
            if p == active:
                ic = color_icons.get(p) or fallback_icons.get(p)
            else:
                ic = bw_icons.get(p) or fallback_icons.get(p)
            if ic is not None:
                c.create_image(iconx, midy, anchor="w", image=ic)
            is_active = (p == active)
            c.create_text(namex, midy, anchor="w", text=PAGE_LABELS.get(p, p),
                          fill=FG,
                          font=("Segoe UI", 9, "bold") if is_active else ("Segoe UI", 9))
            # active page's timer pops: accent color + bold (vs muted otherwise)
            c.create_text(timex, midy, anchor="w", text=fmt_hms(secs),
                          fill=BAR_FILL if is_active else MUTED,
                          font=("Consolas", 9, "bold") if is_active else ("Consolas", 9))
            share = (secs / total) if total > 0 else 0.0
            c.create_rectangle(barx0, y, barx1, y + barh, fill=BAR_BG, outline="")
            fillx = barx0 + (barx1 - barx0) * share
            if fillx > barx0 + 1:
                c.create_rectangle(barx0, y, fillx, y + barh, fill=BAR_FILL, outline="")
            label = "{:.0f}%".format(share * 100)
            if poller.bar_earnings():
                label += "   {}{:,.2f}".format(cur, poller.page_earnings(proj, secs))
            c.create_text((barx0 + barx1) / 2, midy, text=label,
                          fill="#ffffff", font=("Segoe UI", 8, "bold"))

    def refresh():
        proj = poller.current_project
        if poller.connected:
            if poller.running:
                status_var.set(T("tracking"))
                set_app_icon("running")
            elif poller.idle_paused:
                since = poller._idle_since
                elapsed = (time.time() - since) if since else 0
