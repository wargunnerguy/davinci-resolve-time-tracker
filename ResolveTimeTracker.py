def export_csv(poller, path, start=None, end=None):
    """Write a billing CSV. With start/end (date objects, either optional) it
    reports per-project totals for that range from the daily data; with neither
    it reports the full rate-bucket breakdown plus a per-page section. Amounts
    reflect the selected rate view (gross × view factor)."""
    cur = poller.currency()
    factor = poller.rate_factor()
    ranged = bool(start or end)
    projects = sorted(poller.data.get("projects", {}).keys())
    with open(path, "w", newline="", encoding="utf-8-sig") as f:
        w = csv.writer(f)
        rng = "{} .. {}".format(start.isoformat() if start else "…",
                                end.isoformat() if end else "…") if ranged else "All time"
        w.writerow(["Range", rng, "View", poller.rate_view()])
        w.writerow([])
        w.writerow(["Project", "Rate", "Time", "Hours", "Amount ({})".format(cur)])
        gsec = gamt = 0.0
        if ranged:
            for name in projects:
                secs = poller.seconds_in_range(name, start, end)
                if secs <= 0:
                    continue
                rate = poller.rate(name)
                hours = secs / 3600.0
                amt = hours * rate * factor
                gsec += secs
                gamt += amt
                w.writerow([name, "{:g}".format(rate), fmt_hms(secs),
                            "{:.2f}".format(hours), "{:.2f}".format(amt)])
        else:
            for name in projects:
                for rate, secs in poller.rate_breakdown(name):
                    hours = secs / 3600.0
                    amt = hours * rate * factor
                    gsec += secs
                    gamt += amt
                    w.writerow([name, "{:g}".format(rate), fmt_hms(secs),
                                "{:.2f}".format(hours), "{:.2f}".format(amt)])
        w.writerow([])
        w.writerow(["TOTAL", "", fmt_hms(gsec), "{:.2f}".format(gsec / 3600.0),
                    "{:.2f}".format(gamt)])
        if not ranged:
            # Per-page section for precise stats (all-time only).
            w.writerow([])
            w.writerow(["Project", "Page", "Time", "Hours"])
            for name in projects:
                r = poller.rec(name)
                for p in PAGES:
                    secs = r["pages"].get(p, 0)
                    if secs > 0:
                        w.writerow([name, PAGE_LABELS.get(p, p), fmt_hms(secs),
                                    "{:.2f}".format(secs / 3600.0)])


