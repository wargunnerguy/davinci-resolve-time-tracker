def fmt_hms(secs):
    s = max(0, int(round(secs)))
    h, rem = divmod(s, 3600)
    m, sec = divmod(rem, 60)
    return "{:02d}:{:02d}:{:02d}".format(h, m, sec)


def fmt_hm(secs):
    """Hours+minutes only (no seconds) — for the idle counter, so it never
    looks like a running work clock."""
    m_total = max(0, int(secs // 60))
    h, m = divmod(m_total, 60)
    return "{}h {}m".format(h, m) if h else "{}m".format(m)


