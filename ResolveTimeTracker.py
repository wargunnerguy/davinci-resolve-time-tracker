        def _draw_sb():
            sb.delete("all")
            h = sb.winfo_height()
            w = sb.winfo_width()
            first, last = sb_state["first"], sb_state["last"]
            if h <= 1 or last - first >= 1.0:
                return  # nothing to scroll -> no thumb
            thick = max(4, w - 4)
            cx = w / 2
            y0 = first * h + thick / 2
            y1 = max(y0, last * h - thick / 2)
            sb.create_line(cx, y0, cx, y1, width=thick, fill=sb_state["col"],
                           capstyle="round")

        def _on_yscroll(first, last):
            sb_state["first"], sb_state["last"] = float(first), float(last)
            _draw_sb()
        canvas.configure(yscrollcommand=_on_yscroll)
        sb.bind("<Configure>", lambda e: _draw_sb())

        def _sb_press(e):
            h = sb.winfo_height() or 1
            fy = max(0.0, min(1.0, e.y / h))
            first, last = sb_state["first"], sb_state["last"]
            span = last - first
            if not (first <= fy <= last):  # click on track: jump thumb to cursor
                canvas.yview_moveto(max(0.0, min(1.0 - span, fy - span / 2)))
            sb_state["dy"], sb_state["df"] = e.y, sb_state["first"]
            sb_state["col"] = BAR_FILL
            _draw_sb()

        def _sb_drag(e):
            if sb_state["dy"] is None:
                return
            h = sb.winfo_height() or 1
            span = sb_state["last"] - sb_state["first"]
            canvas.yview_moveto(max(0.0, min(
                1.0 - span, sb_state["df"] + (e.y - sb_state["dy"]) / h)))

        def _sb_release(e):
            sb_state["dy"] = None
            sb_state["col"] = MUTED
            _draw_sb()
