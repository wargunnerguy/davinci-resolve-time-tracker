# Launcher role: what Resolve runs from the Scripts menu. No window of its own —
# it starts THIS SAME FILE in system Python as the tracker (or surfaces it).
# ---------------------------------------------------------------------------

def _message_box(text, title="Resolve Time Tracker"):
    try:
        if ctypes is not None and sys.platform == "win32":
            ctypes.windll.user32.MessageBoxW(0, text, title, 0x40)  # information
    except Exception:
        pass


def find_pythonw():
    """Find a windowless system Python (Tkinter-capable). Best-effort."""
    import glob
    import shutil
    cands = [r"C:\Python314\pythonw.exe"]
    cands += sorted(glob.glob(r"C:\Python3*\pythonw.exe"), reverse=True)
    cands += sorted(glob.glob(os.path.join(
        os.environ.get("LOCALAPPDATA", ""),
        r"Programs\Python\Python3*\pythonw.exe")), reverse=True)
    for c in cands:
        if os.path.exists(c):
            return c
    return shutil.which("pythonw") or ""


def run_launcher():
    log("=== launcher invoked (pid {}) ===".format(os.getpid()))
    if lock_is_held_by_other():
        # Already running: surface that window instead of starting a second one.
        log("Tracker already running; requesting bring-to-front.")
        try:
            with open(RAISE_FILE, "w", encoding="utf-8") as f:
                f.write(str(time.time()))
        except Exception:
            log_exception("raise request")
        return
    exe = find_pythonw()
    try:
        me = os.path.abspath(__file__)
    except NameError:
        me = os.path.abspath(sys.argv[0])
    if not exe:
        log("No system Python (pythonw.exe) found.")
        _message_box(
            "Couldn't find Python.\n\nInstall Python 3 for Windows from "
            "python.org (keep the default options), then start the tracker "
            "again from Workspace → Scripts.")
        return
    try:
        flags = 0
        if hasattr(subprocess, "CREATE_NEW_PROCESS_GROUP"):
            flags = subprocess.CREATE_NEW_PROCESS_GROUP | 0x00000008  # DETACHED
        subprocess.Popen([exe, me, "--poller"], cwd=os.path.dirname(me),
                         close_fds=True, creationflags=flags)
        log("Launched tracker: {} {} --poller".format(exe, me))
    except Exception:
        log_exception("launch")
        _message_box(
            "Couldn't start the tracker.\n\nMake sure Python 3 (with Tkinter) "
            "is installed and DaVinci Resolve external scripting is set to "
            "Local. Details in:\n" + LOG_FILE)


def main():
    log("=== tracker starting (pid {}) ===".format(os.getpid()))
    set_app_user_model_id()  # own taskbar identity so our icon (and its live
    #                          status swaps) show on the taskbar, not pythonw's
    if lock_is_held_by_other():
        log("Another poller instance appears to be running; exiting.")
        return
    touch_lock()
    poller = Poller()
    poller.start_reader()  # separate process polls Resolve (keeps UI responsive)
    try:
        while True:
            restart = run_ui(poller)
            if not restart:
                break
            log("Rebuilding UI (language/theme change)")
    except Exception:
        log_exception("Fatal")
    finally:
        poller.stop_reader()
        release_lock()
        log("=== tracker stopped ===")


if __name__ == "__main__":
    if "--reader" in sys.argv:
        ppid = None
        idx = sys.argv.index("--reader")
        if idx + 1 < len(sys.argv):
            try:
                ppid = int(sys.argv[idx + 1])
            except ValueError:
                ppid = None
        run_reader(ppid)
    elif "--poller" in sys.argv:
        main()          # the live Tkinter tracker (system Python)
    else:
        run_launcher()  # no flag = Resolve's Scripts menu started us
